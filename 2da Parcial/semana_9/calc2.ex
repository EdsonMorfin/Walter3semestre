defmodule CalculadoraCientifica do
@moduledoc "En este modulo las funciones calculan el cuadrado y el cubo de un numero y tambien se divide 2 numeros"
  def cuadrado(n1),do: n1*n1
@doc "Funcion cuadrado, calcula el cuadrado de un argumento"
  def cubo(n1), do: n1*n1*n1
@doc "Funcion cubo, calcula el cubo de un argumento"
  def div(n1,n2), do: n1/n2
@doc "Funcion div, calcula la division de 2 argumentos"
end
